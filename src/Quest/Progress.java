package Quest;

public class Progress {
    int progress = 0;
    int detailProgress = 0;

    public void setProgress(int progress) { this.progress = progress; }

    public void setDetailProgress(int detailProgress) { this.detailProgress = detailProgress; }

    public int getProgress() { return progress; }

    public int getDetailProgress() { return detailProgress; }
}
